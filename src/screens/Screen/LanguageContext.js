// Language Provider Component
export const LanguageProvider = ({ children }) => {
  const [selectedLanguage, setSelectedLanguage] = useState('th');
  const [selectedCurrency, setSelectedCurrency] = useState('THB');
  const [isLoading, setIsLoading] = useState(true);

  // โหลดการตั้งค่าภาษาจาก SecureStore เมื่อเริ่มต้น
  useEffect(() => {
    const loadLanguageSettings = async () => {
      try {
        const savedLanguage = await SecureStore.getItemAsync('userLanguage');
        const savedCurrency = await SecureStore.getItemAsync('userCurrency');
        
        if (savedLanguage) {
          setSelectedLanguage(savedLanguage);
        }
        if (savedCurrency) {
          setSelectedCurrency(savedCurrency);
        }
      } catch (error) {
        console.log('Error loading language settings:', error);
      } finally {
        setIsLoading(false);
      }
    };
    
    loadLanguageSettings();
  }, []);

  // ฟังก์ชันเปลี่ยนภาษา
  const changeLanguage = async (newLanguage) => {
    try {
      setSelectedLanguage(newLanguage);
      await SecureStore.setItemAsync('userLanguage', newLanguage);
    } catch (error) {
      console.log('Error saving language:', error);
    }
  };

  // ฟังก์ชันเปลี่ยนสกุลเงิน
  const changeCurrency = async (newCurrency) => {
    try {
      setSelectedCurrency(newCurrency);
      await SecureStore.setItemAsync('userCurrency', newCurrency);
    } catch (error) {
      console.log('Error saving currency:', error);
    }
  };

  // ฟังก์ชันบันทึกการตั้งค่าทั้งหมด
  const saveLanguageSettings = async () => {
    try {
      await SecureStore.setItemAsync('userLanguage', selectedLanguage);
      await SecureStore.setItemAsync('userCurrency', selectedCurrency);
      return true;
    } catch (error) {
      console.log('Error saving language settings:', error);
      return false;
    }
  };

  // ฟังก์ชันแปลข้อความ
  const t = (key) => {
    return translations[selectedLanguage][key] || key;
  };

  // Context value
  const value = {
    selectedLanguage,
    language: selectedLanguage, // alias for backward compatibility
    selectedCurrency,
    changeLanguage,
    changeCurrency,
    saveLanguageSettings,
    t,
    translations: translations[selectedLanguage],
    isLoading,
    availableLanguages: [
      { code: 'th', name: 'ภาษาไทย', flag: '🇹🇭' },
      { code: 'en', name: 'English', flag: '🇺🇸' },
    ],
    availableCurrencies: [
      { code: 'THB', name: 'Thai Baht', symbol: '฿' },
      { code: 'USD', name: 'US Dollar', symbol: '$' },
      { code: 'EUR', name: 'Euro', symbol: '€' },
      { code: 'CNY', name: 'Chinese Yuan', symbol: '¥' },
    ]
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

export default LanguageContext;
